# Assign values to variables
x = 5
y = 10

# Comparison operators (they return True or False)

print(x > y)   # Is x greater than y? → False
print(x < y)   # Is x less than y? → True
print(x == y)  # Are x and y equal? → False
print(x != y)  # Are x and y NOT equal? → True
print(x >= y)  # Is x greater than or equal to y? → False
print(x <= y)  # Is x less than or equal to y? → True


# Comparing int and float
a = 2
b = 2.0

# Even though types are different, values are equal → True
print(a == b)


# String vs integer comparison
number = "20"   # string
num = 20        # integer

# Comparing string with string
print(number != "20")  # Are both strings different? → False