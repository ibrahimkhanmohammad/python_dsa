print("Hello, World!")
print('Hello, World!')

# We can also use the print function to show multiple values, or arguments, at once by separating them with commas. For example:

print("My favourite colors are:","black","white","green","blue")

# Python automatically adds a space between each item when you separate them with commas. This is helpful when you want to print several pieces of information together.